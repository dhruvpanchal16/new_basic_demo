# Vehicle Operator Example

speed1 = 80
speed2 = 60

# Arithmetic Operators
print("Addition =", speed1 + speed2)
print("Subtraction =", speed1 - speed2)
print("Multiplication =", speed1 * 2)
print("Division =", speed1 / 2)

# Comparison Operators
print("speed1 > speed2 =", speed1 > speed2)
print("speed1 < speed2 =", speed1 < speed2)
print("speed1 == speed2 =", speed1 == speed2)

# Logical Operators
print((speed1 > 50) and (speed2 > 50))
print((speed1 > 100) or (speed2 > 50))

# Assignment Operator
speed1 += 20
print("New Speed1 =", speed1)

